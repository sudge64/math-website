use core::panic;
use std::error::Error;

pub fn addition(a: f32, b: f32) -> f32 {
    a + b
}
pub fn subtraction(a: f32, b: f32) -> f32 {
    a - b
}
pub fn multiplication(a: f32, b: f32) -> f32 {
    a * b
}
pub fn division(a: f32, b: f32) -> f32{
            a / b
}
pub fn modulo(a: f32, b: f32) -> f32{
            a % b
}
